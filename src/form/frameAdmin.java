    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        try {
            String c = txtCari.getText();
            String cari = "SELECT * FROM admin WHERE id_admin like '%"+c+"%'" + 
                    "or nama_admin like '%"+c+"%' or username like '%"+c+"%' or userpass like '%"+c+"%'"
                    + " or telp like '%"+c+"%' or email like '%"+c+"%' ";
            objekku.loadData(tabelAdmin, cari);
        } catch (Exception e) {
            System.out.print(e);
        }
    }//GEN-LAST:event_txtCariKeyReleased

    private void tabelAdminMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelAdminMouseClicked
        // TODO add your handling code here:
        int baris = tabelAdmin.getSelectedRow();
        String id = tabelAdmin.getValueAt(baris, 0).toString();
        String nama = tabelAdmin.getValueAt(baris, 1).toString();
        String user = tabelAdmin.getValueAt(baris, 2).toString();
        String pass = tabelAdmin.getValueAt(baris, 3).toString();
        String telp = tabelAdmin.getValueAt(baris, 4).toString();
        String email = tabelAdmin.getValueAt(baris, 5).toString();
        
        txtAdmin.setText(id);
        txtNama.setText(nama);
        txtUser.setText(user);
        txtPass.setText(pass);
        txtTelp.setText(telp);
        txtEmail.setText(email);
        objekku.loadData(tabelAdmin, "SELECT * FROM admin");
    }//GEN-LAST:event_tabelAdminMouseClicked

