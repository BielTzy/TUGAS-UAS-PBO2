    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            objekku.simpanAdminPST(txtAdmin.getText(), txtNama.getText(), txtUser.getText(), txtPass.getText(), txtTelp.getText(), txtEmail.getText());
            if (objekku.validasi == true){
                txtNama.setText(objekku.var_nmAdmin);
                txtUser.setText(objekku.var_userAdmin);
                txtPass.setText(objekku.var_passAdmin);
                txtTelp.setText(objekku.var_telpAdmin);
                txtEmail.setText(objekku.var_emailAdmin);
            }
            objekku.loadData(tabelAdmin, "SELECT * FROM admin");
        } catch (Exception e) {
            
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if(
               txtAdmin.getText().isEmpty() || 
               txtNama.getText().isEmpty() || 
               txtUser.getText().isEmpty() ||
               txtPass.getText().isEmpty() ||
               txtTelp.getText().isEmpty() ||
               txtEmail.getText().isEmpty()
               ){
               JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Terlebih Dahulu");
               tabelAdmin.requestFocus();
            }else{
               objekku.ubahAdminPST(txtAdmin.getText(), 
                       txtNama.getText(), 
                       txtUser.getText(), 
                       txtPass.getText(),
                       txtTelp.getText(),
                       txtEmail.getText());
                objekku.loadData(tabelAdmin, "SELECT * FROM admin");
            }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(
               txtAdmin.getText().isEmpty() || 
               txtNama.getText().isEmpty() || 
               txtUser.getText().isEmpty() ||
               txtPass.getText().isEmpty() ||
               txtTelp.getText().isEmpty() ||
               txtEmail.getText().isEmpty()
                    ) {
                JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Terlebih Dahulu");
                tabelAdmin.requestFocus();
            }else{
                objekku.hapusAdminPST(txtAdmin.getText());
                txtAdmin.setText(null);
                txtNama.setText(null);
                txtUser.setText(null);
                txtPass.setText(null);
                txtTelp.setText(null);
                txtEmail.setText(null);
            }
        objekku.loadData(tabelAdmin, "SELECT * FROM admin");
    }//GEN-LAST:event_btnHapusActionPerformed

