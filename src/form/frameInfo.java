    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try {
            objekku.simpanInfoPST(txtInfo.getText(), txtBerita.getText(), txtKegiatan.getText(), txtTanggal.getText(), txtLaporan.getText());
            if (objekku.validasi == true){
                txtBerita.setText(objekku.var_berita);
                txtKegiatan.setText(objekku.var_kegiatan);
                txtTanggal.setText(objekku.var_tanggalInfo);
                txtLaporan.setText(objekku.var_laporan);
            }
            objekku.loadData(tabelInfo, "SELECT * FROM info");
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        if(
            txtInfo.getText().isEmpty() ||
            txtBerita.getText().isEmpty() ||
            txtKegiatan.getText().isEmpty() ||
            txtTanggal.getText().isEmpty() ||
            txtLaporan.getText().isEmpty()
        ){
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Terlebih Dahulu");
            tabelInfo.requestFocus();
        }else{
            objekku.ubahInfoPST(txtInfo.getText(),
                txtBerita.getText(),
                txtKegiatan.getText(),
                txtTanggal.getText(),
                txtLaporan.getText());
            objekku.loadData(tabelInfo, "SELECT * FROM info");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        if(
            txtInfo.getText().isEmpty() ||
            txtBerita.getText().isEmpty() ||
            txtKegiatan.getText().isEmpty() ||
            txtTanggal.getText().isEmpty() ||
            txtLaporan.getText().isEmpty()
        ) {
            JOptionPane.showMessageDialog(null, "Silahkan Pilih Data Terlebih Dahulu");
            tabelInfo.requestFocus();
        }else{
            objekku.hapusInfoPST(txtInfo.getText());
            txtInfo.setText(null);
            txtBerita.setText(null);
            txtKegiatan.setText(null);
            txtTanggal.setText(null);
            txtLaporan.setText(null);
        }
        objekku.loadData(tabelInfo, "SELECT * FROM info");
    }//GEN-LAST:event_btnHapusActionPerformed

