    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        // TODO add your handling code here:
        try {
            String c = txtCari.getText();
            String cari = "SELECT * FROM info WHERE id_info like '%"+c+"%'" +
            "or berita like '%"+c+"%' or kegiatan like '%"+c+"%' or tanggal_info like '%"+c+"%'"
            + " or laporan like '%"+c+"%' ";
            objekku.loadData(tabelInfo, cari);
        } catch (Exception e) {
            System.out.print(e);
        }
    }//GEN-LAST:event_txtCariKeyReleased

    private void tabelInfoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelInfoMouseClicked
        // TODO add your handling code here:
        int baris = tabelInfo.getSelectedRow();
        String id = tabelInfo.getValueAt(baris, 0).toString();
        String berita = tabelInfo.getValueAt(baris, 1).toString();
        String kegiatan = tabelInfo.getValueAt(baris, 2).toString();
        String tanggal = tabelInfo.getValueAt(baris, 3).toString();
        String laporan = tabelInfo.getValueAt(baris, 4).toString();

        txtInfo.setText(id);
        txtBerita.setText(berita);
        txtKegiatan.setText(kegiatan);
        txtTanggal.setText(tanggal);
        txtLaporan.setText(laporan);
        objekku.loadData(tabelInfo, "SELECT * FROM info");
    }//GEN-LAST:event_tabelInfoMouseClicked

